// Function to reverse a shellcode array in 0x format
void reverseShellcode(unsigned char *shellcode, int size) {
    int i;
    unsigned char temp;
    for (i = 0; i < size/2; i++) {
        temp = shellcode[size-i-1];
        shellcode[size-i-1] = shellcode[i];
        shellcode[i] = temp;
    }
    if (size % 2 != 0) {
        shellcode[size/2] = shellcode[size/2];
    }
}

